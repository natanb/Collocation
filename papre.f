	  SUBROUTINE PAPREP(ILI,TITOLO,ITIPO,IFILE,IQUO,IPOLI,ISPLI,
     .ICOLL,FORMA)
	  IMPLICIT DOUBLE PRECISION(A-H,O-Z)
	  CHARACTER*1 FORMA(80),TITOLO(80)

          IF(ILI.EQ.1) THEN
          WRITE(*,1005)
          READ(*,1000) TITOLO  
1         WRITE(*,1025)
          READ(*,*,ERR=1) ITIPO
2         WRITE(6,1027)
          READ(*,*,ERR=2) IFILE  
3         WRITE(*,1030)
          READ(*,*,ERR=3) IQUO
4         WRITE(*,1032)
          READ(*,*,ERR=4) IPOLI  
5         WRITE(*,1035)
          READ(*,*,ERR=5) ISPLI  
6         WRITE(*,1040)
          READ(*,*,ERR=6) ICOLL  
          WRITE(*,1045)
          READ(*,1000) FORMA
          ELSE
          WRITE(*,1006)
          READ(*,1000) TITOLO  
7         WRITE(*,1026)
          READ(*,*,ERR=7) ITIPO
8         WRITE(6,1028)
          READ(*,*,ERR=8) IFILE  
9         WRITE(*,1031)
          READ(*,*,ERR=9) IQUO
10        WRITE(*,1033)
          READ(*,*,ERR=10) IPOLI  
11        WRITE(*,1036)
          READ(*,*,ERR=11) ISPLI  
12        WRITE(*,1041)
          READ(*,*,ERR=12) ICOLL  
          WRITE(*,1046)
          READ(*,1000) FORMA
          ENDIF
	  RETURN
1000      FORMAT(120A1)
1005      FORMAT(5X,'TITOLO ') 
1006      FORMAT(5X,'TITLE ') 
1025      FORMAT(5X,'TIPO:',/15X,'UNA COMPONENTE = 1', 
     ./15X,'DUE COMPONENTI = 2',
     ./15X,'TRE COMPONENTI = 3')
